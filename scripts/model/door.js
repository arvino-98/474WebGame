function Door(id, width, height, hBoxWidth, hBoxHeight, xPos, yPos, collidable){
    this.id = id;
    this.width = width;
    this.height = height;
    this.hBoxWidth = hBoxWidth;
    this.hBoxHeight = hBoxHeight;
    this.xPos = xPos;
    this.yPos = yPos;
    // this.rotation = rotation;
    this.collidable = collidable;
}